package semana8;

/**
 * Imprimir por pantalla los 10 primeros números enteros
 */
public class EjemploFor01 {

    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }
    }
}

// Los bucles for permiten ejecutar acciones un número conocido de veces
// Está compuesto por 3 partes: for (inicialización; condición; iteración)
package semana8;

/**
Imprimir por pantalla los 5 primeros número enteros ordenados en forma descendente
 */
public class EjemploFor04 {
    public static void main(String[] args) {
        
        for (int i = 10; i >= 5; i -= 1) {
            System.out.println(i);
        }
    }
}
